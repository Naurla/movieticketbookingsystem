    private void showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showActionPerformed
        String selected = (String) comboMovie.getSelectedItem();
        if (selected == null) return;

        String movieID = selected.split(" - ")[0];
        //    int movieID = Integer.parseInt(selected.split(" - ")[0]);
        try {
            String tableName = "movie " + movieID + " - shows";
            String query = "SELECT ID, showTime, capacity, availableSeats, place FROM `" + tableName + "`;";
            ResultSet rs = database.getStatement().executeQuery(query);

            DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Show Time", "Capacity", "Available Seats", "Place"}, 0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getString("showTime"),
                    rs.getInt("capacity"),
                    rs.getInt("availableSeats"),
                    rs.getString("place")
                });
            }
            tableShowTimes.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_showActionPerformed

