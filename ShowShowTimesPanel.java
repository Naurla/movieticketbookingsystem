    private void loadMovieIDs(){
        try{
            comboMovie.removeAllItems();
            ResultSet rs = database.getStatement().executeQuery("SELECT ID, name FROM movies;");
            while(rs.next()){
                int id = rs.getInt("ID");
                String name = rs.getString("name");
              
                comboMovie.addItem(id + " - "+ name);
            }
            
            
        }catch (SQLException e){
            e.printStackTrace();
        }
    }
